-- @param a –Ω–∞—á–∞–ª–æ –¥–∏–∞–ø–∞–∑–æ–Ω–∞
-- @param b –∫–æ–Ω–µ—Ü –¥–∏–∞–ø–∞–∑–æ–Ω–∞
-- @return —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –ø–∞—Ä –ø—Ä–æ—Å—Ç—ã—Ö —á–∏—Å–µ–ª, —Å—É–º–º–∞ –∫–æ—Ç–æ—Ä—ã—Ö —Ä–∞–≤–Ω–∞ —á–µ—Ç–Ω—ã–º —á–∏—Å–ª–∞–º –≤ [a..b]

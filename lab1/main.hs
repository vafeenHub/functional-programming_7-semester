-- | –ù–∞—Ö–æ–¥–∏—Ç –≤—Å–µ –Ω–∞—Ç—É—Ä–∞–ª—å–Ω—ã–µ –ø–∞—Ä—ã (x, y), —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä—è—é—â–∏–µ —É—Ä–∞–≤–Ω–µ–Ω–∏—é x^2 + y^2 = k^2
-- @param k –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ (–≥–∏–ø–æ—Ç–µ–Ω—É–∑–∞)
-- @return —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –ø–∞—Ä (x, y), –≥–¥–µ x^2 + y^2 = k^2
findSolutions :: Int -> [(Int, Int)]
findSolutions k =
  [ (x, y)
    | x <- [1 .. k],
      y <- [1 .. k],
      x * x + y * y == k * k
  ]


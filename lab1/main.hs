-- | –ü–æ–¥—Å—á–∏—Ç—ã–≤–∞–µ—Ç –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π —á–∏—Å–ª–∞ n
-- @param n —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ
-- @return –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π —á–∏—Å–ª–∞ n
countDivisors :: Int -> Int
countDivisors n = length [d | d <- [1 .. abs n], n `mod` d == 0]

-- | –ù–∞—Ö–æ–¥–∏—Ç –≤—Å–µ —á–∏—Å–ª–∞ –∏–∑ –¥–∏–∞–ø–∞–∑–æ–Ω–∞ [a..b], —É –∫–æ—Ç–æ—Ä—ã—Ö —Ä–æ–≤–Ω–æ k –¥–µ–ª–∏—Ç–µ–ª–µ–π.
-- @param a –Ω–∞—á–∞–ª–æ –¥–∏–∞–ø–∞–∑–æ–Ω–∞
-- @param b –∫–æ–Ω–µ—Ü –¥–∏–∞–ø–∞–∑–æ–Ω–∞
-- @param k —Ç—Ä–µ–±—É–µ–º–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –¥–µ–ª–∏—Ç–µ–ª–µ–π
-- @return —Å–ø–∏—Å–æ–∫ —á–∏—Å–µ–ª —Å k –¥–µ–ª–∏—Ç–µ–ª—è–º–∏
findNumbersWithKDivisors :: Int -> Int -> Int -> [Int]
findNumbersWithKDivisors a b k = [x | x <- [a .. b], countDivisors x == k]


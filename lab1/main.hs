-- @param hectares —Å–ø–∏—Å–æ–∫ –ø–ª–æ—â–∞–¥–µ–π –ø–æ—Å–µ–≤–æ–≤ –≤ –≥–µ–∫—Ç–∞—Ä–∞—Ö –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Ä–∞–π–æ–Ω–∞
-- @param yields —Å–ø–∏—Å–æ–∫ —Å—Ä–µ–¥–Ω–µ–π —É—Ä–æ–∂–∞–π–Ω–æ—Å—Ç–∏ –ø—à–µ–Ω–∏—Ü—ã –≤ —Ç–æ–Ω–Ω–∞—Ö —Å –≥–µ–∫—Ç–∞—Ä–∞ –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Ä–∞–π–æ–Ω–∞
-- @return –∫–æ—Ä—Ç–µ–∂ (—Å—Ä–µ–¥–Ω—è—è —É—Ä–æ–∂–∞–π–Ω–æ—Å—Ç—å –ø–æ –≤—Å–µ–π —Ç–µ—Ä—Ä–∏—Ç–æ—Ä–∏–∏, –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –ø—à–µ–Ω–∏—Ü—ã)
calculateWheatStats :: [Double] -> [Double] -> (Double, Double)
calculateWheatStats hectares yields =
  let totalHectares = sum hectares -- –°—É–º–º–∞ –≤—Å–µ—Ö –≥–µ–∫—Ç–∞—Ä–æ–≤
      totalYield = sum (zipWith (*) hectares yields) -- –û–±—â–∏–π —Ç–æ–Ω–Ω–∞–∂: –ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –≥–µ–∫—Ç–∞—Ä–æ–≤ –Ω–∞ —É—Ä–æ–∂–∞–π–Ω–æ—Å—Ç—å
      averageYield = if totalHectares == 0 then 0 else totalYield / totalHectares -- –°—Ä–µ–¥–Ω—è—è —É—Ä–æ–∂–∞–π–Ω–æ—Å—Ç—å
   in (averageYield, totalYield)


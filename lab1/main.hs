-- | –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —á–∏—Å–ª–æ –ø—Ä–æ—Å—Ç—ã–º
-- @param x —Ü–µ–ª–æ–µ —á–∏—Å–ª–æ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
-- @return True, –µ—Å–ª–∏ —á–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–æ–µ, –∏–Ω–∞—á–µ False
isPrime :: Int -> Bool
isPrime x
  | x < 2 = False -- –ß–∏—Å–ª–∞ –º–µ–Ω—å—à–µ 2 –Ω–µ –ø—Ä–æ—Å—Ç—ã–µ
  | otherwise = checkDivisors 2 -- –ü—Ä–æ–≤–µ—Ä–∫–∞ –¥–µ–ª–∏—Ç–µ–ª–µ–π –æ—Ç 2 –¥–æ –∫–æ—Ä–Ω—è –∏–∑ x
  where
    limit = floor (sqrt (fromIntegral x)) -- –í–µ—Ä—Ö–Ω—è—è –≥—Ä–∞–Ω–∏—Ü–∞ —Å –ø–ª–∞–≤–∞—é—â–µ–π —Ç–æ—á–∫–æ–π –ø—Ä–æ–≤–µ—Ä–∫–∏ –¥–µ–ª–∏—Ç–µ–ª–µ–π
    checkDivisors current
      | current > limit = True -- –ï—Å–ª–∏ –ø—Ä–æ–≤–µ—Ä–∏–ª–∏ –≤—Å–µ –¥–µ–ª–∏—Ç–µ–ª–∏, —á–∏—Å–ª–æ –ø—Ä–æ—Å—Ç–æ–µ
      | x `mod` current == 0 = False -- –ù–∞–π–¥–µ–Ω –¥–µ–ª–∏—Ç–µ–ª—å, —á–∏—Å–ª–æ —Å–æ—Å—Ç–∞–≤–Ω–æ–µ
      | otherwise = checkDivisors (current + 1) -- –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å–ª–µ–¥—É—é—â–∏–π –¥–µ–ª–∏—Ç–µ–ª—å

-- | –ù–∞—Ö–æ–¥–∏—Ç –≤—Å–µ –ø–∞—Ä—ã –ø—Ä–æ—Å—Ç—ã—Ö —á–∏—Å–µ–ª, —Å—É–º–º–∞ –∫–æ—Ç–æ—Ä—ã—Ö —Ä–∞–≤–Ω–∞ n (—Ç–æ–ª—å–∫–æ –¥–ª—è —á–µ—Ç–Ω—ã—Ö n > 2)
-- @param n —á–µ—Ç–Ω–æ–µ —á–∏—Å–ª–æ (> 2)
-- @return —Å–ø–∏—Å–æ–∫ –∫–æ—Ä—Ç–µ–∂–µ–π (p, q), –≥–¥–µ p –∏ q –ø—Ä–æ—Å—Ç—ã–µ, p+q = n
goldbachPairsForNumber :: Int -> [(Int, Int)]
goldbachPairsForNumber n
  | n <= 2 = [] -- –ù–µ—Ç —Ä–µ—à–µ–Ω–∏–π –¥–ª—è —á–∏—Å–µ–ª <= 2
  | odd n = [] -- –ù–µ—Ç —Ä–µ—à–µ–Ω–∏–π –¥–ª—è –Ω–µ—á–µ—Ç–Ω—ã—Ö —á–∏—Å–µ–ª
  | otherwise = findPairs 2 [] -- –ò—â–µ–º –ø–∞—Ä—ã –Ω–∞—á–∏–Ω–∞—è —Å 2
  where
    half = n `div` 2 -- –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º —á–∏—Å–ª–∞ –¥–æ –ø–æ–ª–æ–≤–∏–Ω—ã n
    -- –†–µ–∫—É—Ä—Å–∏–≤–Ω–æ –ø–µ—Ä–µ–±–∏—Ä–∞–µ–º –∫–∞–Ω–¥–∏–¥–∞—Ç—ã p, —Å–æ–±–∏—Ä–∞–µ–º –ø–æ–¥—Ö–æ–¥—è—â–∏–µ –ø–∞—Ä—ã –≤ acc
    findPairs p acc
      | p > half = acc -- –ï—Å–ª–∏ p –ø—Ä–µ–≤—ã—Å–∏–ª –ø–æ–ª–æ–≤–∏–Ω—É, –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –Ω–∞–∫–æ–ø–ª–µ–Ω–Ω—ã–µ –ø–∞—Ä—ã
      | isPrime p && isPrime (n - p) = findPairs (p + 1) ((p, n - p) : acc)
      | otherwise = findPairs (p + 1) acc


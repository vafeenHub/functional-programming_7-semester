-- | –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —á–∏—Å–ª–æ —Å–æ–≤–µ—Ä—à–µ–Ω–Ω—ã–º (—Ä–∞–≤–Ω–æ —Å—É–º–º–µ —Å–≤–æ–∏—Ö –¥–µ–ª–∏—Ç–µ–ª–µ–π, –∫—Ä–æ–º–µ —Å–∞–º–æ–≥–æ —á–∏—Å–ª–∞)
-- @param num –Ω–∞—Ç—É—Ä–∞–ª—å–Ω–æ–µ —á–∏—Å–ª–æ –¥–ª—è –ø—Ä–æ–≤–µ—Ä–∫–∏
-- @return True, –µ—Å–ª–∏ —á–∏—Å–ª–æ —Å–æ–≤–µ—Ä—à–µ–Ω–Ω–æ–µ, –∏–Ω–∞—á–µ False
isPerfect :: Int -> Bool
isPerfect num = sum (divisors num) == num
  where
    divisors x = [d | d <- [1 .. x - 1], x `mod` d == 0]

-- | –ù–∞—Ö–æ–¥–∏—Ç –≤—Å–µ —Å–æ–≤–µ—Ä—à–µ–Ω–Ω—ã–µ —á–∏—Å–ª–∞ –º–µ–Ω—å—à–µ n
-- @param n –≤–µ—Ä—Ö–Ω—è—è –≥—Ä–∞–Ω–∏—Ü–∞ –ø–æ–∏—Å–∫–∞ (–Ω–µ –≤–∫–ª—é—á–∏—Ç–µ–ª—å–Ω–æ)
